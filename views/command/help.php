Berikut adalah perintah yang dapat anda gunakan:

1. akun
    melihat detail akun

2. harga
    melihat kategori pulsa

3. harga *kategori*
    melihat harga per kategori

4. beli *kode* *nomor pelanggan*
    melakukan pembelian pulsa

5. deposit
    melakukan deposit

6. admin
    memunculkan kontak admin

7. riwayat
    melihat 10 riwayat pembelian terakhir

8. riwayat deposit
    melihat 10 riwayat deposit terakhir

9. status 'id' / cek order 'id'
    untuk cek status pembelian

10. deposit 'id' / cek deposit 'id'
    untuk cek status deposit

11. tentang / about
    keterangan BOT ini

12. saran / masukan
    Memberi saran kepada admin